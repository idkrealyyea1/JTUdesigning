let currentLang = 'en';

function applyTranslations(lang) {
  currentLang = lang;
  const t = translations[lang];
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.body.classList.toggle('lang-ar', lang === 'ar');

  // Update all elements with data-key
  document.querySelectorAll('[data-key]').forEach(el => {
    const key = el.getAttribute('data-key');
    if (!t[key]) return;
    if (el.tagName === 'INPUT') {
      el.placeholder = t[key];
    } else {
      el.innerHTML = t[key];
    }
  });

  // Update placeholder on search input
  const searchInput = document.getElementById('search-input');
  if (searchInput) searchInput.placeholder = t['search_placeholder'];

  // Update lang toggle active state
  document.querySelectorAll('.lang-en, .lang-ar').forEach(el => {
    el.classList.remove('active');
  });
  const activeLangEl = document.querySelector(lang === 'en' ? '.lang-en' : '.lang-ar');
  if (activeLangEl) activeLangEl.classList.add('active');

  // Re-render products and FAQ with new language
  if (typeof renderProducts === 'function') renderProducts();
  if (typeof renderFAQ === 'function') renderFAQ();
  if (typeof renderTestimonials === 'function') renderTestimonials();

  localStorage.setItem('juthoor-lang', lang);
}

function getText(key) {
  return translations[currentLang][key] || translations.en[key] || key;
}

function initLanguage() {
  const savedLang = localStorage.getItem('juthoor-lang') || 'en';
  applyTranslations(savedLang);

  const langToggle = document.getElementById('lang-toggle');
  if (langToggle) {
    langToggle.addEventListener('click', () => {
      applyTranslations(currentLang === 'en' ? 'ar' : 'en');
    });
  }
}

// Will be called after DOM loaded
window.getText = getText;
window.currentLang = () => currentLang;
window.initLanguage = initLanguage;
window.applyTranslations = applyTranslations;
